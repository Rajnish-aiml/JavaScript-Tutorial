//Identifiers = Names jo programmer variables, functions, classes, etc. ke liye banata hai.

// Rules for Identifiers:

// Only letters, digits, underscore _, aur dollar sign $ use kar sakte ho.

// Valid: userName, _temp, $value1

// Invalid: first-name, 123abc, user name

// Digit se start nahi kar sakte.

// Valid: a1, value2

// Invalid: 1abc

// Case-sensitive hote hain.

// name ≠ Name ≠ NAME (teeno alag variables)

// Reserved keywords ka use nahi kar sakte.

// Invalid: var, for, class, function

// Unicode characters allow hain (advance me emojis bhi).

let age = 25;          // age is an identifier
function myFunction() { } // myFunction is an identifier
class Student {}       // Student is an identifier

